#define FA2_TOKEN_REGISTRY_SIZE \
    (sizeof(FA2_TOKEN_REGISTRY) / sizeof(FA2_TOKEN_REGISTRY[0]))

_Static_assert(FA2_TOKEN_REGISTRY_SIZE <= (size_t)INT16_MAX + 1U,
               "FA2 token indices must fit in int16_t");

bool
fa2_token_idx_valid(int16_t idx)
{
    return idx >= 0 && (size_t)idx < FA2_TOKEN_REGISTRY_SIZE;
}

const fa2_token_metadata_t *
fa2_token_by_index(int16_t idx)
{
    if (!fa2_token_idx_valid(idx)) {
        return NULL;
    }
    return &FA2_TOKEN_REGISTRY[idx];
}

int16_t
fa2_token_index(const fa2_token_metadata_t *token)
{
    ptrdiff_t idx = token - FA2_TOKEN_REGISTRY;
    if (idx < 0 || (size_t)idx >= FA2_TOKEN_REGISTRY_SIZE) {
        return -1;
    }
    return (int16_t)idx;
}

